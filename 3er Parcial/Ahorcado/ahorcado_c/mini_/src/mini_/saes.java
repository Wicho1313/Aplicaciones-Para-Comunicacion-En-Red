    private void conectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conectActionPerformed
        // TODO add your handling code here:
        int dificultad = 0;
        if(facil.isEnabled()== false) dificultad = 1;
        if(medio.isEnabled()== false) dificultad = 2;
        if(dificil.isEnabled()== false) dificultad = 3;
        if(dificultad!=0){
            try {
                Socket cl = new Socket("192.168.0.7", 1234);
            //    System.out.println("Conectado a : " + cl.getInetAddress().getHostName());
                  PrintWriter pw = new PrintWriter(new OutputStreamWriter(cl.getOutputStream()));
                   pw.println(dificultad);
                    pw.flush();//Limpiar bufer despues de enviar msj
                    BufferedReader br = new BufferedReader(new InputStreamReader(cl.getInputStream()));
                    msj = br.readLine();
                //   muestra.setText(msj);
                   pw.close();
                   br.close();
                cl.close();
                juego j = new juego();
                j.setVisible(true);
                this.dispose();   
            } catch (IOException ex) {
                muestra.setText("Error al conectar con el servidor!");
            }
        }else{
            conect.setBackground(Color.red);
        }
    }//GEN-LAST:event_conectActionPerformed

